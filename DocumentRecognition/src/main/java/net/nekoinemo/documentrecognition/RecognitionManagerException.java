package net.nekoinemo.documentrecognition;

public class RecognitionManagerException extends Exception {

	public RecognitionManagerException(String message) {
		super(message);
	}
	public RecognitionManagerException(String message, Throwable cause) {
		super(message, cause);
	}
	public RecognitionManagerException(Throwable cause) {
		super(cause);
	}
}
